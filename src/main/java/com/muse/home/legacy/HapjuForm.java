package com.muse.home.legacy;

import java.util.Date;

public class HapjuForm {
    private String date;
    private String time;
    private String hapjuInfo;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getHapjuInfo() {
        return hapjuInfo;
    }

    public void setHapjuInfo(String hapjuInfo) {
        this.hapjuInfo = hapjuInfo;
    }
}
